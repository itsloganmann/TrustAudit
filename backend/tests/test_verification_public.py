class TestVerifyPublicHappyPath:
    def test_returns_sanitized_envelope(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal)
        r = client.get(f"/api/verify/{invoice_id}")
        assert r.status_code == 200
        body = r.json()
        assert body["invoice_id"] == invoice_id
        assert body["state"] == "VERIFIED"
        assert body["watermark"] == "Generated by TrustAudit"
        assert isinstance(body["audit_hash"], str)
        assert len(body["audit_hash"]) == 64
        # Hash is hex
        int(body["audit_hash"], 16)
        assert body["confidence_score"] == 0.94
        assert body["date_of_acceptance"] == "2026-03-21"
        assert body["extraction_model"] == "gemini-flash-latest"

    def test_no_pii_in_response(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal)
        r = client.get(f"/api/verify/{invoice_id}")
        body_text = r.text
        # The vendor name, GSTIN, invoice number, and amount must NEVER appear.
        assert "Sharma Steel" not in body_text
        assert "29ABCDE1234F1Z5" not in body_text
        assert "INV-SECRET-001" not in body_text
        assert "999999" not in body_text
        assert "Bharat Industries" not in body_text
        assert "AAACB1234D" not in body_text

        # Defensive: ensure response keys are exactly the allowed set
        body = r.json()
        allowed_keys = {
            "invoice_id",
            "audit_hash",
            "state",
            "confidence_score",
            "date_of_acceptance",
            "extracted_at",
            "extraction_model",
            "watermark",
        }
        assert set(body.keys()) == allowed_keys

    def test_submitted_state_also_visible(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal, state="SUBMITTED_TO_GOV")
        r = client.get(f"/api/verify/{invoice_id}")
        assert r.status_code == 200
        assert r.json()["state"] == "SUBMITTED_TO_GOV"

    def test_audit_hash_is_deterministic(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal)
        r1 = client.get(f"/api/verify/{invoice_id}")
        r2 = client.get(f"/api/verify/{invoice_id}")
        assert r1.json()["audit_hash"] == r2.json()["audit_hash"]


class TestVerifyPublic404:
    def test_nonexistent_invoice_returns_404(self, client):
        r = client.get("/api/verify/9999")
        assert r.status_code == 404

    def test_pending_state_is_hidden(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal, state="PENDING")
        r = client.get(f"/api/verify/{invoice_id}")
        # Don't leak existence of unverified invoices
        assert r.status_code == 404

    def test_disputed_state_is_hidden(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal, state="DISPUTED")
        r = client.get(f"/api/verify/{invoice_id}")
        assert r.status_code == 404

    def test_needs_info_is_hidden(self, client, SessionLocal):
        invoice_id = _seed_invoice(SessionLocal, state="NEEDS_INFO")
        r = client.get(f"/api/verify/{invoice_id}")
        assert r.status_code == 404
