def _seed_invoice(SessionLocal, *, state="VERIFIED") -> int:
    s = SessionLocal()
    try:
        ent = Enterprise(name="Bharat Industries", pan="AAACB1234D")
        s.add(ent)
        s.flush()
        msme = MSME(
            enterprise_id=ent.id,
            vendor_name="Sharma Steel Works",
            gstin="29ABCDE1234F1Z5",
        )
        s.add(msme)
        s.flush()
        invoice = Invoice(
            vendor_name="Sharma Steel Works",
            gstin="29ABCDE1234F1Z5",
            invoice_number="INV-SECRET-001",
            invoice_amount=999_999.99,
            invoice_date=date(2026, 3, 21),
            date_of_acceptance=date(2026, 3, 21),
            deadline_43bh=date(2026, 5, 5),
            status=state,
            state=state,
            confidence_score=0.94,
            enterprise_id=ent.id,
            msme_id=msme.id,
            verified_at=datetime(2026, 3, 21, 14, 1, 9),
        )
        s.add(invoice)
        s.commit()
        return invoice.id
    finally:
        s.close()


